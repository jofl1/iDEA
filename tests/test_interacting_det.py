    """build_labelled_state must keep the determinant amplitudes accessible
    for power users."""
    benchmark = _load_benchmark_module()
    s = benchmark.CASES[case_name]()
    det_state = det.solve(s, k=0)
    labelled = det.build_labelled_state(s, det_state)

